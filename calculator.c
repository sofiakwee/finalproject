#include <stdio.h>

   float multiply(float x)
   {
     float product = (x * x);
     return product;
   }

   float divide(float x)
   {
     float quotient = (x / x);
     return quotient;
   }

   float add(float x)
   {
     float sum = (x + x);
     return sum;
   }

   float subtract(float x)
   {
     float difference = (x - x);
     return difference;
   }
